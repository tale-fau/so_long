#include "libft.h"

size_t	ft_tablen(char **s)
{
	size_t	i;
	int		j;

	i = 0;
	if (s == NULL)
		return (0);
	while (s[i])
	{
		j = 0;
		while (s[i][j])
			j++;
		i++;
	}
	return (i);
}
